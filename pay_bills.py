def pay_bills(month, bills):
    # Проверяем, кратен ли номер месяца трём (месяцы с премией: 3, 6, 9, 12...)
    if month % 3 == 0:
        # Оплачиваем все счета, кроме первого и последнего
        return bills[1:-1]
    else:
        # Оплачиваем только первый и последний счёт
        return [bills[0], bills[-1]]

print(pay_bills(5, ['Интернет', 'Коммуналка', 'Телефон', 'Страховка']))